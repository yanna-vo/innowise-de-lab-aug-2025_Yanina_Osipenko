fruits = ["apple", "banana"]
print("Исходный список:", fruits)

fruits.append("orange")
print("Добавляем 'orange' в конец списка:", fruits)

fruits.insert(1, "grape")
print("Вставляем 'grape' по индексу 1:", fruits)

fruits.remove("banana")
print("Удаляем 'banana':", fruits)

fruits.sort()
print("Сортируем список:", fruits)

fruits.reverse()
print("Перевернули список:", fruits)